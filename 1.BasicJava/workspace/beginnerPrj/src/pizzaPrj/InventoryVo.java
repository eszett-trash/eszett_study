package pizzaPrj;

public class InventoryVo {
	private int id;
	private String name;
	private int remine;
	private int price;
	private int kind;
	private int use;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getRemine() {
		return remine;
	}
	public void setRemine(int remine) {
		this.remine = remine;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	public int getKind() {
		return kind;
	}
	public void setKind(int kind) {
		this.kind = kind;
	}
	public int getUse() {
		return use;
	}
	public void setUse(int use) {
		this.use = use;
	}

	
}
