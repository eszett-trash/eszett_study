package ice_VO;

public class IceCreamVO {
	private String icecreamId; // 제품의 ID 값
	private String icecreamName; // 제품명
	private int icecreamNum; // 제품의 수량 저장
	
	public String getIcecreamId() {
		return icecreamId;
	}
	public void setIcecreamId(String icecreamId) {
		this.icecreamId = icecreamId;
	}
	public String getIcecreamName() {
		return icecreamName;
	}
	public void setIcecreamName(String icecreamName) {
		this.icecreamName = icecreamName;
	}
	public int getIcecreamNum() {
		return icecreamNum;
	}
	public void setIcecreamNum(int icecreamNum) {
		this.icecreamNum = icecreamNum;
	}
	
}
